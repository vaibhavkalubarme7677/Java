package inheritance;

public class calculator {

	void add(int i,int j) {
		System.out.println("Addition Result= "+(i+j));
	}
	void sub(int i,int j) {
		System.out.println("Substration Result= "+(i-j));
	}
	void mul(int i,int j) {
		System.out.println("Multiplication Result= "+(i*j));
	}
	void div(int i,int j) {
		System.out.println("Division Result= "+(i/j));
	}
	void rem(int i,int j) {
		System.out.println("Reminder Result= "+(i%j));
	}
}
	
